# 1 - A fórmula para calcular a área de uma circunferência é: área = π . 𝑟𝑎𝑖𝑜2
# Considerando para este problema que π é constante e vale 3.14159.


print("Esse Programa calcula a área de uma circunferencia")
pi = 3.14159
raio = float(input("\nDigite um raio para definir a circunferencia: "))
area = pi * (raio**2)
print(f"\nA área da circunferencia é: {area:.2f}\n")
